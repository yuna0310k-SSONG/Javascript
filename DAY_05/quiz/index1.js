const def1 = data.map((v) => {
  v.fullName = v.fullName.split(" ")[1].toLowerCase(); //full name을 last 만 있도록 바꾸고 소문자화

  if (v.time.includes("AM")) {
    //24시간 화
    v.time = v.time.slice(0, v.time.length - 3);
  } else {
    v.time = v.time.slice(0, v.time.length - 3);
    v.time = v.time.split(":");
    v.time = +v.time[0] + 12 + ":" + v.time[1];
  }

  v.timeZone = v.timeZone.split("/")[0];
  return v;
});
